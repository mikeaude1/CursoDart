/**
 * Un operador es un simbolo que le dice  al copilador
 * que debe de realizar una tarea matematica, relacional o logica
 *y debe de producir un resultado
 */
main() {
  int a = 10 + 5; // + 15
  a = 20 - 10; // - 10
  a = 10 * 2; // * 20

  double b = 10 / 2; // / 5.0
  b = 10 % 3; // % 1 El sobrante de la division
  b = -b; // -expr Es usado para negar el valor de la expresion b
  int c =
      10 ~/
      3; // ~/ 3 Divicion comun y corrente. El resultado de la division entera
  int d = 1;
  d++; //  d = d + 1 incrementa en 1 el valor de d
  d--; //  d = d - 1 decrementa en 1 el valor de d
  d += 2; // d = d + 2 incrementa en 2 el valor de d
  d -= 2; // d = d - 2 decrementa en 2 el valor de d
}
